// Calculator App

const display = document.getElementById('display');


// from the 'keys' id, create the three necessary functions below
function appendToDisplay(input){
    display.value += input;
}

function clearDisplay(){
    display.value = "";
}

function calculate(){
    try{
        display.value = eval(display.value);
    }
    catch(error){
        display.value = "Error";
    }
}